x = 10
y = 5
print(f"=== OPERATOR ARITMATIKA ===")
print(f"{x} + {y} = {x+y}")
print(f"{x} - {y} = {x-y}")
print(f"{x} * {y} = {x*y}")
print(f"{x} / {y} = {x/y}")
print("\n=== OPERATOR PERBANDINGAN ===")
print(f"x > y ? {x>y}")
print(f"x == y ? {x == y}")
print(f"x <= y ? {x <= y}")
print("\n=== OPERATOR LOGIKA ===")
a = True
b = False
print(f"a and b = {a and b}")
print(f"a or b = {a or b}")
print(f"not a = {not a}")
